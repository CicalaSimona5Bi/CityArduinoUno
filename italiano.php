<h2>LA FAMIGLIA E L'INFANZIA</h2>

<h3><p>Nato il 9 marzo 1883 a Trieste (città che apparteneva allora all'Impero austro-ungarico), figlio di un padre discendente da una nobile famiglia veneziana e madre che apparteneva a una famiglia ebrea di piccoli commercianti.</p></h3>
<h3><p>Egli fu abbandonato dal padre ancor prima di essere venuto al mondo.</p></h3>
<h3><p>Saba trascorre un'infanzia difficile e malinconica, che rievocherà nelle sue poesie.</p></h3>


<br>

<h2>INCONTRO CON L'OPERA: IL CANZONIERE</h2>

<h3><p style="text-align: justify;"> L'edizione definitiva dell'opera risulta divisa in sezioni, che sono a loro volta raggruppate in tre "volumi" (la "tripartizione" risale al modello illustre della <i>Commedia</i> dantesca, ma si trova anche in altre opere, tra cui il <i>Poema paradisiaco</i> di d'Annunzio), corrispondenti al tempo della: giovinezza, maturità e vecchiaia.</p>
